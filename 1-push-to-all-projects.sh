##################################################################################################################
# Colors
##################################################################################################################
if command -v tput >/dev/null 2>&1 && [[ -t 1 ]]; then
    RED="$(tput setaf 1)"
    GREEN="$(tput setaf 2)"
    YELLOW="$(tput setaf 3)"
    BLUE="$(tput setaf 4)"
    CYAN="$(tput setaf 6)"
    RESET="$(tput sgr0)"
else
    RED="" GREEN="" YELLOW="" BLUE="" CYAN="" RESET=""
fi

##################################################################################################################
# Logging
##################################################################################################################
log_section() {
    echo
    echo "${GREEN}##################################################################################################################${RESET}"
    echo "$1"
    echo "${GREEN}##################################################################################################################${RESET}"
    echo
}

log_info() {
    echo
    echo "${BLUE}##################################################################################################################${RESET}"
    echo "$1"
    echo "${BLUE}##################################################################################################################${RESET}"
    echo
}

log_warn() {
    echo
    echo "${YELLOW}##################################################################################################################${RESET}"
    echo "$1"
    echo "${YELLOW}##################################################################################################################${RESET}"
    echo
}

log_error() {
    echo
    echo "${RED}##################################################################################################################${RESET}"
    echo "$1"
    echo "${RED}##################################################################################################################${RESET}"
    echo
}

log_section "Pushing all changes to the existing githubs - scanning existing folders"

echo "Type comment of your commit"
read -r input

count=0

for name in */; do
    count=$((count + 1))
    cd "$name"
    log_section "Github $count : $name"

    git pull

    git add --all .
    git commit -m "$input" || true

    branch=$(git symbolic-ref --short HEAD)
    log_info "Pushing to branch: $branch"
    git push -u origin "$branch"

    log_info "$(basename "$(pwd)") done"
    cd ..
done
